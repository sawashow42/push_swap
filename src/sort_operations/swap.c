#include "../../includes/push_swap.h"

void	swap(t_stack **stack)
{
	t_stack	*head;
	t_stack	*follow;

	if (!(*stack) || !(*stack)->next)
		return ;
	head = *stack;
	follow = head->next;
	head->next = follow->next;
	follow->next = head;
	*stack = follow;
}

void	sa(t_stack **stack_a)
{
	swap(stack_a);
	write(1, "sa\n", 3);
}

void	sb(t_stack **stack_b)
{
	swap(stack_b);
	write(1, "sb\n", 3);
}

void	ss(t_stack **stack_a, t_stack **stack_b)
{
	swap(stack_a);
	swap(stack_b);
	write(1, "ss\n", 3);
}
